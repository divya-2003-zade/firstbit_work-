//Print a solid square pattern
//Input: n = 4
#include<stdio.h>
int main()
{
	for(int n=1; n<=4; n++)
	{
		for(int m=1; m<=4; m++)
		{
			printf("* ");
		}
		printf("\n");
	}
}                                                                                                                                                                                                                                                                                                                       
